package com.org.gnos.scheduler.processor;

public class CapexRecord {

	private int capexNo;
	private int instanceNo;
	private int year;
	private int value;
	
	public int getCapexNo() {
		return capexNo;
	}
	public void setCapexNo(int capexNo) {
		this.capexNo = capexNo;
	}
	public int getInstanceNo() {
		return instanceNo;
	}
	public void setInstanceNo(int instanceNo) {
		this.instanceNo = instanceNo;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
	
}
